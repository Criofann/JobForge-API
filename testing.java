public class Example {
    public static void main(String[] args) {
        int x = 1;
        switch (x) {
            case 1:
                System.out.println("x is equal to 1");
                break;
            case 2:
                System.out.println("x is equal to 2");
                break;
            default:
                System.out.println("x is not equal to 1 or 2");
                break;
        }
    }
}

